package com.pokewords.framework.engine.exceptions;

public class SpriteDeclaratorException extends GameEngineException {


    public SpriteDeclaratorException(String message) {
        super(message);
    }

    public SpriteDeclaratorException(String message, Throwable cause) {
        super(message, cause);
    }

    public SpriteDeclaratorException(Throwable cause) {
        super(cause);
    }

    public SpriteDeclaratorException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
